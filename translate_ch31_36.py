def translate(txt):
    """Apply dictionary then single-char punctuation, then clean up."""
    if not txt:
        return txt
    s = txt
    # Apply multi-character dictionary (longest first), with spaces around each
    for k in sorted(D.keys(), key=lambda x: -len(x)):
        s = s.replace(k, f" {D[k]} ")
    # Apply single-char punctuation fallbacks
    for ch, en in SINGLE_CHAR.items():
        s = s.replace(ch, en)
    # Post-process
    s = clean(s)
    return s


def process_chapter(ch):
    inp = os.path.join(BASE, f"pharma_cn_ch{ch:02d}.csv")
    out = os.path.join(BASE, f"pharma_en_ch{ch:02d}.csv")
    print(f"\n=== Chapter {ch} ===")
    with open(inp, "r", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        rows = list(reader)
        fns = reader.fieldnames
    out_rows = []
    for row in rows:
        nr = {}
        for k in fns:
            v = row.get(k, "")
            if k == "id":
                nr[k] = v
            elif k == "correct":
                nr[k] = v.strip()
            elif k in ("type", "question", "explanation", "category"):
                nr[k] = translate(v)
            elif k.startswith("option_"):
                nr[k] = translate(v)
            else:
                nr[k] = v
        out_rows.append(nr)
    with open(out, "w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fns)
        w.writeheader()
        w.writerows(out_rows)
    print(f"  {len(out_rows)} rows -> {out}")
    for i in range(min(3, len(out_rows))):
        r = out_rows[i]
        q = r['question']
        if len(q) > 130:
            q = q[:130] + "..."
        print(f"  [{r['id']}] [{r['type']}] {q}")


