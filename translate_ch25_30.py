# Sort translations by Chinese term length (longest first) for greedy matching
_TRANSLATIONS.sort(key=lambda x: len(x[0]), reverse=True)

# CRITICAL: Remove all entries whose "Chinese" key is actually ASCII/Latin text.
# These cause English output corruption (e.g., "ug" -> "mcg" turns "drugs" -> "drmcgs",
# "NA" -> "norepinephrine" corrupts any word containing "na").
_TRANSLATIONS = [(cn, en) for cn, en in _TRANSLATIONS
                 if any(ord(c) > 127 for c in cn)]


def translate_text(text):
    """Translate Chinese text to English using the longest-match dictionary."""
    if not text or not isinstance(text, str):
        return text

    # Handle standalone True/False
    t = text.strip()
    if t == "正确":
        return "True"
    if t == "错误":
        return "False"

    result = text
    for cn, en in _TRANSLATIONS:
        if cn in result:
            result = result.replace(cn, en)

    # Clean up spacing
    result = " ".join(result.split())
    return result


